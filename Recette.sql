CREATE TABLE IF NOT EXISTS `TYPE_INGREDIENTS` (
											  `ID_TYPE_INGREDIENT`  SMALLINT UNSIGNED AUTO_INCREMENT,
											  `NOM_TYPE_INGREDIENT` VARCHAR(50) NOT NULL,
											  PRIMARY KEY (`ID_TYPE_INGREDIENT`),
											  UNIQUE KEY (`NOM_TYPE_INGREDIENT`)) ENGINE = InnoDB
																				  DEFAULT CHARSET = `UTF8`;

CREATE TABLE IF NOT EXISTS `TYPE_RECETTES` (
										   `ID_TYPE_RECETTES`  SMALLINT UNSIGNED AUTO_INCREMENT,
										   `NOM_TYPE_RECETTES` VARCHAR(50) NOT NULL,
										   PRIMARY KEY (`ID_TYPE_RECETTES`),
										   UNIQUE KEY (`NOM_TYPE_RECETTES`)) ENGINE = InnoDB
																			 DEFAULT CHARSET = `UTF8`;
